package com.FaysalTask5;

public class Date {
	public int day,month,year;
	  public Date(){
	    day=this.day;
	    month=this.month;
	    year=this.year;
	  }
	  public int getDay(){
	    return day;
	  }
	  public void setDay(int d){
	    day=d;
	  }

	  public int getMonth(){
	     return month;
	  }
	  public void setMonth(int m){
	    month=m;
	  }
	  public int getYear(){
	   return year;
	  }
	  public void setYear(int y){
	    year=y;
	  }
}
